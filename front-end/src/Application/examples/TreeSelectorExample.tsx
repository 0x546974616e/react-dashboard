function makeId(): string {
  return Math.random().toString(36).slice(2);
}

function makeRetailers(parent: Tree, depth: number): Tree[] {
  const r = Math.random() * 0.5 + 0.2;
  return retailers.filter(() => Math.random() < r).map(
    retailer => {
      const tree: Tree = {
        id: makeId(),
        label: retailer,
        children: null,
        parent,
        depth,
      };

      tree.children = {
        label: "Staff",
        nodes: makeStaff(tree, depth + 1),
      };

      return tree;
    }
  );
}

function makeStaff(parent: Tree, depth: number): Tree[] {
  const r = Math.random() * 0.9 + 0.1;
  return firstNames.filter(() => Math.random() < r).map(
    staff => ({
      id: makeId(),
      label: `${staff} ${lastNames[Math.floor(Math.random() * lastNames.length)]}`,
      children: null,
      parent,
      depth,
    })
  );
}

const root: Tree = {
  id: makeId(),
  label: "Europe",
  parent: null,
  children: null,
  depth: 0,
};

root.children = {
  label: "Countries",
  nodes: Object.entries(countries).map(
    ([ country, cities ]) => {
      const tree: Tree = {
        id: makeId(),
        label: country,
        children: null,
        parent: root,
        depth: 1,
      }

      tree.children = {
        label: "Cities",
        nodes: cities.map(
          city => {
            const subtree: Tree = {
              id: makeId(),
              label: city,
              parent: tree,
              children: null,
              depth: 2,
            }

            subtree.children = {
              label: "Retailers",
              nodes: makeRetailers(subtree, 3),
            };

            return subtree;
          }
        ),
      };

      return tree;
    }
  ),
};

export function TreeSelectorExample(): JSX.Element {
  return (
    <TreeSelector
      root={root}
    />
  );
}
